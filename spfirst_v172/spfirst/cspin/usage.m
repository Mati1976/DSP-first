%----------------------------------------------------------------------------
% Using the CSPIN Graphical User Interface
%----------------------------------------------------------------------------
%
%----------------------------------------------------------------------------
% Overview
%----------------------------------------------------------------------------
%
%   Visualize phasors spinning in the complex plane and the resulting real
%   and imaginary parts of the signal, together with the corresponding 
%   spectral lines.
%
%   Shows the initial state (t=0) when called, then select "Play" 
%   to start rolling.
%
%----------------------------------------------------------------------------
% Theory
%----------------------------------------------------------------------------
%
%----------------------------------------------------------------------------
% CSPIN Controls
%----------------------------------------------------------------------------
% CASES:
%           * Single Phasor
%           * Phasors: same frequency
%           * Spectral lines: different frequencies
%           * Beat
%           * Harmonics of a periodic signal
%
% Changing the Line Width:
%
%   By clicking on the Set Line Width menu, the user can change the widths of 
%   all the graph lines.  This can be useful when using this program in a 
%   class lecture to make sure the lines are visible from the back of a 
%   lecture room.
%----------------------------------------------------------------------------
